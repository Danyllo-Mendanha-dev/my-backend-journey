//switch statement: é útil para verificar constantes ou variáveis com vário valores diferentes
const grade = 'F'

switch(grade){

    case 'A':
        console.log('Você tirou um A')
        break
    case 'B':
        console.log('Você tirou um B')
        break
    case 'C':
        console.log('Você tirou um C')
        break
    case 'D':
        console.log('Você tirou um D')
        break
    case 'E':
        console.log('Você tirou um E')
        break
    default:
        console.log('nota inválida')
}




















